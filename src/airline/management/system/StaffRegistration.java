    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        //Save Button
        String Sname = name.getText();
        String Suname = uname.getText();        
        String Spswrd = pswrd.getText();
        String Smail = mail.getText();
        String Snic = nic.getText();
        String Spay = payment.getText();
        String Saddress = address.getText();
        String Scontact = contact_no.getText();
        String Sgender = genderbox.getSelectedItem()!= null ? genderbox.getSelectedItem().toString() : null;
        String Sshift = shiftbox.getSelectedItem() != null ? shiftbox.getSelectedItem().toString() : null;
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection(jdbcUrl, user, dbpassword);
            
            if (Sname.equals("")||Suname.equals("")||Spswrd.equals("")||Smail.equals("")||Snic.equals("")||Spay.equals("")||Saddress.equals("")||Scontact.equals("")||Sgender == null || Sshift == null)
            {
                JOptionPane.showMessageDialog(this, "Some Fields are empty or not selected");   
            }
            else
            {
                pst = con.prepareStatement("insert into staff(Name,Gender,NIC,User_Name,Password,Email,Address,Contact_No,Payment,Shift) values (?,?,?,?,?,?,?,?,?,?)");
                pst.setString(1, Sname);
                pst.setString(2, Sgender);            
                pst.setString(3, Snic);
                pst.setString(4, Suname);
                pst.setString(5, Spswrd);
                pst.setString(6, Smail);
                pst.setString(7, Saddress);
                pst.setString(8, Scontact);
                pst.setString(9, Spay);
                pst.setString(10, Sshift);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this,"Staff Added.");
            
                name.setText("");            
                uname.setText("");
                pswrd.setText("");
                mail.setText("");
                nic.setText("");            
                payment.setText("");
                address.setText("");
                contact_no.setText("");
                genderbox.setSelectedIndex(-1);
                shiftbox.setSelectedIndex(-1);
                name.requestFocus();
                table_update();
            }
            con.close(); 
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StaffRegistration.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(StaffRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_addActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        //For Edit Button
        DefaultTableModel d1 = (DefaultTableModel)jTable1.getModel();
        int selectIndex = jTable1.getSelectedRow();
        
        String Sname = d1.getValueAt(selectIndex, 1).toString();
        String Sgender = genderbox.getSelectedItem() != null ? genderbox.getSelectedItem().toString() : null;
        String Snic = nic.getText();
        String Suname = uname.getText();        
        String Spswrd = pswrd.getText();
        String Smail = mail.getText();
        String Saddress = address.getText();
        String Scontact = contact_no.getText();
        String Spay = payment.getText();
        String Sshift = shiftbox.getSelectedItem() != null ? shiftbox.getSelectedItem().toString() : null;
            
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection(jdbcUrl, user, dbpassword);
            
            if (Sname.equals("")||Suname.equals("")||Spswrd.equals("")||Smail.equals("")||Snic.equals("")||Spay.equals("")||Saddress.equals("")||Scontact.equals("")|| Sshift == null || Sgender == null)
            {
                JOptionPane.showMessageDialog(this, "Some Fields are empty or not selected");   
            }
            else
            { 
                pst = con.prepareStatement("update staff set Name = ?, Gender =?, User_Name = ?, Password = ?, Email = ?, Address = ?, Contact_No = ?, Payment = ?, Shift = ? WHERE NIC = ?"); 
                pst.setString(1, Sname);
                pst.setString(2, Sgender);            
                pst.setString(3, Suname);
                pst.setString(4, Spswrd);
                pst.setString(5, Smail);
                pst.setString(6, Saddress);
                pst.setString(7, Scontact);
                pst.setString(8, Spay);
                pst.setString(9, Sshift);
                pst.setString(10, Snic);
                pst.executeUpdate();
           
                JOptionPane.showMessageDialog(this, "Record Updated");
           
                name.setText("");            
                uname.setText("");
                pswrd.setText("");
                mail.setText("");
                nic.setText("");            
                payment.setText("");
                address.setText("");
                contact_no.setText("");
                genderbox.setSelectedIndex(-1);
                shiftbox.setSelectedIndex(-1);
                name.requestFocus();
                table_update();
            }
            con.close(); 
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StaffRegistration.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(StaffRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_editActionPerformed

