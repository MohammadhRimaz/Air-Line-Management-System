    public void LoadCusID()
    {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection(jdbcUrl,user,password);
            pst = con.prepareStatement("select * from customer");
            rs = pst.executeQuery();
            cusid.removeAllItems();
            
            while (rs.next())
            {
                cusid.addItem(rs.getString(1));
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Feedback.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Feedback.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    public void tableupdate (){
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection(jdbcUrl,user,password);
            pst = con.prepareStatement("select * from feedback");
            rs = pst.executeQuery();
            ResultSetMetaData rd = rs.getMetaData();
            int c = rd.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                
                for (int i =1; i<=c; i++)
                {
                    v2.add(rs.getString("Customer_ID"));
                    v2.add(rs.getString("Customer_Name"));
                    v2.add(rs.getString("Feedback"));
                }
                df.addRow(v2);
            }
            con.close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Feedback.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Feedback.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
