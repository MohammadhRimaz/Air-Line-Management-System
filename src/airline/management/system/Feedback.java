    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // Add Button
        String Cid = cusid.getSelectedItem().toString();
        String Cname = cusname.getText();
        String fdbck = feedback.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection (jdbcUrl,user,password);
            
            if (Cname.equals("")||fdbck.equals(""))
            {
                JOptionPane.showMessageDialog(this, "Some fields are Empty!");
            }
            else
            {
                pst = con.prepareStatement("insert into feedback(Customer_ID,Customer_Name,Feedback) values (?,?,?)");
                pst.setString(1, Cid);
                pst.setString(2, Cname);
                pst.setString(3, fdbck);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Feedback Added!");
                
                cusid.setSelectedIndex(-1);
                cusname.setText("");
                feedback.setText("");
                cusid.requestFocus();
                tableupdate();
            }
            con.close();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Feedback.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Feedback.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_addActionPerformed

    private void cusidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cusidActionPerformed
        //get all Customers id...
         //when a cus id clicked then particular customer's details will be display in next text fields.. 
        if (cusid.getSelectedIndex() != -1) 
        {
 
            try {
                String Cid = cusid.getSelectedItem().toString();
                Class.forName("com.mysql.cj.jdbc.Driver");
                con = DriverManager.getConnection(jdbcUrl,user,password);
                
                 pst1 = con.prepareStatement("select * from customer where Cus_ID = ?");
                pst1.setString(1, Cid);
                rs1 = pst1.executeQuery();
              
                if (rs1.next()) 
                {
                    // Retrieve customer details from the database
                    String customerName = rs1.getString("Customer_Name");

                    // Populate the text fields with retrieved information
                    cusname.setText(customerName);
                } 
                else
                {
                    JOptionPane.showMessageDialog(this, "Customer Not Found");
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Feedback.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Feedback.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }  
    }//GEN-LAST:event_cusidActionPerformed

